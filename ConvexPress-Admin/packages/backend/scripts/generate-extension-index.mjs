#!/usr/bin/env node
/**
 * generate-extension-index.mjs
 *
 * Scans the two extension roots:
 *   - packages/backend/convex/extensions/<id>/schema.ts       (official, tracked)
 *   - packages/backend/convex/extensions.local/<id>/schema.ts (local, gitignored)
 *
 * Emits packages/backend/convex/schema/_extensionsIndex.generated.ts
 * which the main `schema.ts` hub imports + spreads. Every `schema.ts`
 * file in either root is expected to export `tables` (a record of
 * Convex `defineTable(...)` calls).
 *
 * Output shape:
 *   import { tables as <slug>Tables } from "../extensions/<id>/schema";
 *   import { tables as <slug>LocalTables } from "../extensions.local/<id>/schema";
 *   export const extensionTables = { ...<slug>Tables, ...<slug>LocalTables };
 *
 * If no extensions exist on disk, emits `export const extensionTables = {};`
 *
 * Idempotent. Safe to run repeatedly. Output is gitignored.
 *
 * Hooked into package.json scripts:
 *   - predev / predeploy: runs before convex dev / convex deploy
 *   - codegen:extensions: standalone manual run
 */

import { readdirSync, existsSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const BACKEND_ROOT = resolve(HERE, "..");                 // packages/backend/
const CONVEX_DIR = join(BACKEND_ROOT, "convex");
const OFFICIAL_DIR = join(CONVEX_DIR, "extensions");
const LOCAL_DIR = join(CONVEX_DIR, "extensions.local");
const SCHEMA_DIR = join(CONVEX_DIR, "schema");
const OUT_PATH = join(SCHEMA_DIR, "_extensionsIndex.generated.ts");

/**
 * Convert an extension id (e.g., "case-studies") to a JS-safe identifier
 * prefix (e.g., "caseStudies") for use in import/spread statements.
 */
function toIdentifier(id) {
	const camel = id.replace(/[-_](.)/g, (_, c) => c.toUpperCase());
	// Strip any other unsafe chars; identifier must start with a letter.
	return camel.replace(/[^a-zA-Z0-9_]/g, "");
}

/**
 * List extension folders that contain a schema.ts at the top level.
 * Returns an array of { id, slug, importPath } objects.
 *
 * `root` is the absolute folder path; `relativeFrom` is the path that
 * import statements inside the generated file should resolve against
 * (it lives at convex/schema/_extensionsIndex.generated.ts, so imports
 * to convex/extensions/<id>/schema are "../extensions/<id>/schema").
 */
function findSchemas(root, importPrefix) {
	if (!existsSync(root)) return [];
	const entries = readdirSync(root, { withFileTypes: true });
	const out = [];
	for (const entry of entries) {
		if (!entry.isDirectory()) continue;
		// Skip metadata folders like .gitkeep helpers, node_modules, etc.
		if (entry.name.startsWith(".") || entry.name === "node_modules") continue;
		const schemaPath = join(root, entry.name, "schema.ts");
		if (!existsSync(schemaPath)) continue;
		out.push({
			id: entry.name,
			slug: toIdentifier(entry.name),
			importPath: `${importPrefix}/${entry.name}/schema`,
		});
	}
	return out;
}

function generate() {
	const official = findSchemas(OFFICIAL_DIR, "../extensions");
	const local = findSchemas(LOCAL_DIR, "../extensions.local");

	// Disambiguate identifier collisions between official and local sources
	// by suffixing local identifiers with "Local".
	const officialSeen = new Set(official.map((e) => e.slug));
	for (const ext of local) {
		if (officialSeen.has(ext.slug)) {
			ext.slug = `${ext.slug}Local`;
		}
	}

	const all = [...official, ...local];

	const header = [
		"// AUTO-GENERATED — DO NOT EDIT BY HAND.",
		"// Regenerated by packages/backend/scripts/generate-extension-index.mjs.",
		"// Run `bun run codegen:extensions` (or it runs automatically as predev/predeploy).",
		"//",
		"// Scans convex/extensions/* (official, tracked) AND convex/extensions.local/* (user, gitignored)",
		"// for any folder containing a top-level `schema.ts` that exports `tables`.",
		"// All discovered tables are merged into a single `extensionTables` object",
		"// which the main schema hub spreads into `defineSchema`.",
		"",
	].join("\n");

	if (all.length === 0) {
		const body = "export const extensionTables = {};\n";
		mkdirSync(SCHEMA_DIR, { recursive: true });
		writeFileSync(OUT_PATH, header + body, "utf-8");
		return { official: 0, local: 0, outPath: OUT_PATH };
	}

	const imports = all
		.map((e) => `import { tables as ${e.slug}Tables } from "${e.importPath}";`)
		.join("\n");

	const spreads = all.map((e) => `\t...${e.slug}Tables,`).join("\n");

	const body = [
		imports,
		"",
		"export const extensionTables = {",
		spreads,
		"};",
		"",
	].join("\n");

	mkdirSync(SCHEMA_DIR, { recursive: true });
	writeFileSync(OUT_PATH, header + body, "utf-8");
	return { official: official.length, local: local.length, outPath: OUT_PATH };
}

const result = generate();
const localCount = result.local;
const officialCount = result.official;
const total = officialCount + localCount;
console.log(
	`[generate-extension-index] wrote ${result.outPath}  ` +
		`(${officialCount} official, ${localCount} local, ${total} total)`,
);
